<?php
// ============================================
// doctor/logout_doctor.php - ডাক্তার লগআউট
// অ্যাডমিন ড্যাশবোর্ড বা ডাক্তার লগইন পেজে যাবে
// ============================================

// সেশন স্টার্ট
if (session_status() == PHP_SESSION_NONE) {
    session_start();
}

// ============================================
// চেক করুন অ্যাডমিন থেকে লগইন করেছিল কিনা
// ============================================
$login_from_admin = $_SESSION['login_from_admin'] ?? false;

// ============================================
// ডাক্তারের সেশন মুছুন
// ============================================
unset($_SESSION['doctor_logged_in']);
unset($_SESSION['doctor_id']);
unset($_SESSION['doctor_nid']);
unset($_SESSION['doctor_name']);
unset($_SESSION['doctor_login_time']);
unset($_SESSION['login_from_admin']);

// ============================================
// অ্যাডমিন সেশন অক্ষুণ্ণ রাখুন
// ============================================

// ============================================
// Redirect based on login source
// ============================================
if ($login_from_admin) {
    // অ্যাডমিন থেকে লগইন করেছিল → অ্যাডমিন ড্যাশবোর্ডে যাবে
    header("Location: ../admin/dashboard.php");
} else {
    // নরমাল লগইন → ডাক্তার লগইন পেজে যাবে
    header("Location: login.php");
}
exit();
?>